<h2> Lesson 6 - The Spanish To-be twins </h2>

In English and most other languages we simply use the verb "to be" to accordingly conjugate it to speak about our personalities, condition, professions, feelings, status. Eg I <b>am</b> Anita, you <b>are</b> a doctor, he <b>is</b> unwell, I <b>am</b> angry, we <b>are</b> married, they <b>are</b> rich etc. If you observe these sentences, you will see that some of them are permanent in nature while others keep changing frequently and are temporary in nature. This can somewhat explain why we have 2 different verbs for <b>to be</b> in Spanish.

<b>Ser</b> and <b>Estar</b> are the 2 verbs equivalent to <b>to be</b> in English. However they are not used interchangeably and used with a purpose to differentiate between activities of a permanant or lasting nature and those that undergo change very frequently or are of a temporary nature. While this explanation gives us a fair idea of where to use which verb but it is not a practical or even assured way. The best way is to couple this tool with a classification of where we need to use which verb. Ser and Estar must be used as per the below classification. The first letters form the acronyms <b>Doctor</b> and <b>Place</b> and make it easy to remmember.

