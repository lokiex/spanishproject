<h2>	Lesson 5 - I am and You are	</h2>		
				
It would be helpful to learn how to conjugate the verb <b>to be</b> in Spanish. Here the intention is just to introduce you to one part of the verb but the Spanish <b>to be</b> has a twin sibling ! We will learn about it  soon in the forthcoming lessons. The infinitive or the base form of the verb <b>to be</b> that we are discussing in this lesson, in Spanish is <b>ser</b>. <b>Ser</b> in the below tables can only be used to describe someone, introduce oneself, speak about ones occupation, relationships, origins and about time. Mostly everything that is permanent in nature.			
<br>
<br>				
<ul>				
<li>	I am	-	Yo soy	</li>
<li>	You are	-	Tú eres	</li>
<li>	He is	-	Él es	</li>
<li>	She is	-	Ella es	</li>
<li>	You are  formal	-	Usted es	</li>
<li>	We are	-	Nosotros somos	</li>
<li>	You all are	-	Vosotros sois	</li>
<li>	They are	-	Ellos son	</li>
<li>	You all are Formal	-	Ustedes son	</li>
</ul>				
     <h4>Example</h4>			
<ul>				
<li>	I am John	-	Soy Juan (in spanish John is Juan)	</li>
<li>	You are my friend	-	Tú eres mi amiga	</li>
<li>	She is Eva	-	Ellas es Eva	</li>
<li>	He is Tom	-	Él es Tom	</li>
<li>	They are men	-	Ellos son hombres	</li>
<li>	We are girls	-	Ellas son chicas	</li>
<li>	You all are good.	-	Vosotros sois buenos.	</li>
</ul>				
        <h4> Key points to remember </h4>			
<ul>				
<li>	You can simply use <b> Soy </b> since this conjugated form cannot be used for any other person except for the <b>I</b> form.			
<li>	Similarly the use of <b>Tú</b> is optional. So you can simply say <b>eres</b>			
<li>	Note that the <b>Tú</b> must have the accent on <b>u</b>. Without the accent it means <b>your</b>.			
<li>	It would be helpful to remember that the <b>He</b> <b>She</b> <b>Formal You</b> forms have the same conjugations always for all the verbs.			
<li>	Note that the <b>Él</b> must have the accent on <b>E</b>. Without the accent it means <b>the</b> <i>masculine</i> .			
<li>	Similarly the <b>They</b> and <b>Formal You all</b> have the same conjugated forms always for all the verbs.			
<li>	The <b>Nosotros</b> <b>Nosotras</b> <b>Vosotros</b> <b>Vosotras</b>  <b>Ellos</b> <b>Ellas</b> have the exactly same conjugations i.e. the change in gender does not affect the verb in any way.			
</ul>				

