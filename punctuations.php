<h2 id="1">Lesson 2 - Tildes and punctuations</h2>
<p>When we pronounce words, we pronounce syllables and not phonemes alone. Phonemes are the smallest units of speech which can be distinguished from each other by their unique sounds. A syllable is a part of a word which usually has a vowel that might or might not be surrounded by consonants. A syllable can also be a whole word when it is too small to break. In Spanish we have <strong>open and closed syllables</strong> known as <strong>s&iacute;laba abierta</strong> and <strong>s&iacute;laba cerrada</strong>. Those that end in a vowel are know as open while those ending in a consonant are known as closed. Further on syllables fall under 2 categories either stressed syllables or unstressed syllables known as <strong>s&iacute;labas &aacute;tonas</strong> or <strong>s&iacute;labas t&oacute;nicas</strong> respectively. The syllable that is pronounced with a stress is known as <strong>s&iacute;laba t&oacute;nica</strong> and the remaining are known as <strong>s&iacute;labas &aacute;tonas</strong> Needless to mention there can only be 1 stressed syllable in a word. In the Spanish language, the vowels <strong>i and u</strong> are considered weak vowels while <strong>a,e and o</strong> are considered strong vowels. One way to remember this is I and You (i and u) these are weak [easy to remember I and you are weak]&nbsp; remaining are strong.</p>
<a href="#top" class="top">Scroll to top</a><h4 id="2">Dipthongs (Diptongos)</h4>
<p>The combination of a strong vowel plus a weak vowel following each other in any order or both the weak vowels together are known as Dipthongs or Diptongos. There are 14 such combinations possible and these are <strong>ai, ia, au, ua, ei, ie, eu, ue, oi, io, ou, uo</strong> and <strong>iu, ui</strong></p>
<a href="#top" class="top">Scroll to top</a><h4 id="3">Triphthongs (Triptongos)</h4>
<p>A syllable is said to have a triphthong when a strong vowel is sandwiched between 2 weak and unstressed vowels such as <strong>iai, iei, uai, uei, uau</strong></p>
<a href="#top" class="top">Scroll to top</a><h4 id="4">Hiatus (Hiatos)</h4>
<p>The combination of a strong vowel and a weak stressed (accented) vowel or 2 strong vowels or 2 repeated vowels together are known as a Hiatus.</p>
<p>Example</p>
<p>1 strong vowel and a weak stressed (accented) vowels <strong>a&uacute;, e&iacute;, e&uacute;, &iacute;a, &iacute;o, &iacute;e, o&iacute;, o&uacute;, &uacute;a, &uacute;e, &uacute;o</strong></p>
<p>2 strong vowels one after the other <strong>eo, oe, ae, ea</strong></p>
<p>2 repeated vowels one after the other <strong>aa, ee, ii, oo, uu</strong></p>
<p>It should be noted that when we pronounce gue, gui, que, qui these are pronounced with u being silent hence there is not dipthong in this case. Once we use the <strong>&uuml;</strong> like g&uuml;e g&uuml;i then in this case we do have a dipthong.</p>
<p>If the ui combination is positioned at the end of the word and with an accent it will still be considered a dipthong. morro<strong>qu&iacute;</strong></p>
<p>If the silent aphabet <strong>h</strong> comes between the vowels it does not stop a Dipthong or Triphtong from being formed.</p>
<a href="#top" class="top">Scroll to top</a><h4 id="5">The tilde</h4>
<p>An <strong>accent &acute;</strong> in spanish is known as <strong>tilde</strong>. All words in Spanish have one syllable that is stressed but will not necessarily have an accent or a tilde on them. There are rules that determine which syllable will have an accent on their vowel so as to force a stress on that particular syllable.</p>
<a href="#top" class="top">Scroll to top</a><h4 id="6">Rules for last syllable stressed - Palabras agudas</h4>
<p>All words which have more than one syllable (polysyllabic) and end in a vowel or the consonants <strong>n</strong> or <strong>s</strong>, will also have an accent on the vowel of the last syllable.</p>
<p>Examples <em>A-za-fr&aacute;n, com-p&aacute;s, bis-tu-r&iacute;, so-f&aacute;, bam-b&uacute;. cam-pe-&oacute;n, es-co-c&eacute;s, ma-n&iacute;, es-cri-bi&oacute;, Pe-r&uacute;</em>.</p>
<a href="#top" class="top">Scroll to top</a><h4 id="">Exceptions:</h4>
<p>An accent should not be used on syllables which end in 2 consonant even if they happen to be <strong>ns</strong> or those ending in diptongos such as <strong>ay, ey and oy</strong></p>
<p>Example: <em>Ca-nals, Cla-rens, Ca-sals, etc. Pa-ra-guay, Mon-te-rrey, es-toy, con-voy, ca-rey </em></p>
<p>All polysyllabic words with a stress on the last syllable and ending in any other consonant other than n or s do not have an accent or tilde. Den-tal, an-sie-dad, a-za-har, pa-pel, a-ves-truz, re-loj, na-riz, per-fil, re-loj, etc.</p>
<p>Exception: Those words which have a hiatus formed due to the combination of a strong unstressed vowel plus a weak stressed vowel, will have an accent even though they do not end in <strong>n</strong> or <strong>s</strong> <em>Ma&iacute;z = ma/&iacute;z o&iacute;r = o/&iacute;r ra&iacute;z = ra/&iacute;z Re&iacute;r = re/&iacute;r Ra&uacute;l = Ra/&uacute;l ata&uacute;d = a-ta/&uacute;d </em></p>
<a href="#top" class="top">Scroll to top</a><h4 id="7">Rules for second last syllable stressed - Palabras graves o llanas</h4>
<p>All polisyllabic words with a stress on the second last syllable and not ending in <strong>n,s or a vowel</strong> will have an accent. See exception 1</p>
<p><em>L&iacute;-der, t&oacute;-rax, &aacute;r-bol, m&aacute;r-tir, ca-r&aacute;c-ter, c&eacute;s-ped, cr&aacute;-ter, C&eacute;-sar, &aacute;s-pid, &eacute;-ter, etc. </em></p>
<p>All polisyllabic words with a stress on the second last syllable and ending in <strong>n,s or a vowel</strong> will NOT have an accent. See exception 2</p>
<p><em>Cer-ta-men, ti-je-ras, re-su-men, ca-si, a-dor-no, ven-ta-na, za-pa-tos, etc. </em></p>
<p>Exceptions:</p>
<p>Exception 1 All polisyllabic words with a stress on the second last syllable and ending in a combination of 2 consonants, even if the last one is <strong>s</strong>, will have an accent. B&iacute;-ceps, f&oacute;r-ceps, tr&iacute;-ceps</p>
<p>Exception 2 All polisyllabic words with a stress on the second last syllable and forming a hiatus will still have a tilde as per the rule for hiatus. Ma-r&iacute;-a, tr&iacute;-o, si-t&uacute;-a, tran-v&iacute;-a, etc.</p>
<p>Exception 3 Those words which have a hiatus formed within the word will have an accent even though they end in <strong>n</strong> or <strong>s</strong> B&uacute;ho = b&uacute;/ho desle&iacute;do = des-le/&iacute;-do coh&iacute;ben = co/h&iacute;-ben O&iacute;do = o/&iacute;-do re&uacute;ne = re/&uacute;-ne moh&iacute;no = mo/h&iacute;-no</p>
<p>Note: When syllables form a hiatus due to 2 strong vowels coming together, the general rule for putting accents will apply and there is no exception here.</p>
<p><em>L&iacute;-ne-a, h&eacute;-ro-e, cr&aacute;-ne-o, n&uacute;-cle-o, te-rr&aacute;-que-o, a-&eacute;-re-o, pe-&oacute;n, le-&oacute;n, me-di-te-rr&aacute;-ne-o</em></p>
<a href="#top" class="top">Scroll to top</a><h4 id="8">Rules for third to last syllable stressed - Palabras <strong>esdr&uacute;julas</strong></h4>
<p>All 3rd to last syllable stressed words have an accent.</p>
<p><em>L&aacute;m-pa-ra, V&oacute;-mi-to, &Iacute;n-cli-to, &aacute;-bre-se-las, &Aacute;l-ge-bra </em></p>
<a href="#top" class="top">Scroll to top</a><h4 id="9">Rules for fourth to last syllable stressed - Palabras <strong>sobreesdr&uacute;julas</strong></h4>
<p>All 4th to last syllable stressed words have an accent.</p>
<a href="#top" class="top">Scroll to top</a><h4 id="10">The monosyllables</h4>
<p>Monosyllables or words formed by a single syllable do not have an accent.</p>
<p><em>Dios, vi, van, mes, di, da, fue, as, mar, Juan, pie, res, vio, dio, luz, yo, Luis, cal, mal, sal, pues, doy, vio, fe, etc. </em></p>
<p>Exceptions</p>
<p>Except in cases where there is a need to differentiate 2 words with the same letters but a different meaning known as tilde diacr&iacute;tica in Spanish. These are 8 in total <strong>el, se, te, si, mi, tu, mas y de.</strong></p>
<p>Except in cases where there is a requirement to pronounce a word with emphasis eg. <em>qu&eacute;, qui&eacute;n, cu&aacute;ndo, c&oacute;mo, d&oacute;nde, cu&aacute;l, cu&aacute;nto. Such an accent is known as tilde enf&aacute;tica</em>.</p>
<a href="#top" class="top">Scroll to top</a><h4 id="11">Other notes</h4>
<p>When we add the adverb <strong>mente</strong> to words with an accent they maintain their accents and those without an accent remain without one.</p>
<p>We use the accent on <strong>&oacute; spanish translation of or</strong> in order to avoid confusion while using it in between numbers so that it is not mistaken to be a Zero. eg.El compr&oacute; 5 &oacute; 10 cajas de cerveza para su matrimonio. Such a tilde is known as la tilde desambig&uuml;izadora. If no numbers involved then we dont use the accent on the <strong>o</strong> eg. Ella debe tener catorce o quince a&ntilde;os. Adding <strong>me, te, le, la, lo</strong> might requires to put a tilde in the word as per the general rules of accents</p>
<p><em>eg. Traigan + me = tr&aacute;iganme Busca + te = b&uacute;scate Pide + le = p&iacute;dele Pon + te +la = p&oacute;ntela Informa + me + lo = inf&oacute;rmamelo Sienta + se = si&eacute;ntase </em></p>
<p>For compound words if the word does not have a hyphen then the first word with the accent will lose it's accent.</p>
<p>For compound words if the word has a hyphen then all the words will keep their accents.</p>
