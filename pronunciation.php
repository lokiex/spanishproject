<h2>Lesson 1 - Spanish Alphabets and Sounds</h2>
</div>
<div class="row">
<div class="col-3">Spanish language consists of 27 alphabets out of which there are five vowels and 22 consonants. The pronunciation is one of the most simplest. The words are literally spelt as the alphabets sound. Let's have a look at each and everyone of them.&nbsp;</div>
