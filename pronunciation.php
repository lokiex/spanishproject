<p><strong>Pronunciation</strong></p>
<p><em>English - [Kh]hey*</em></p>
<p><em>Spanish - ge</em></p>
<p><strong>Notes</strong></p>
<ul>
<li class="col-7">Try whispering the sound of kh and then actually say the word hey. Now do it fast. Whisper kh and say alound hey. You will notice that you are actually trying to position the back of your tongue as if you are about to pronounce Kh but instead you stop and then pronounce hey from that initial Kh position.</li>
<li>When G is followed by either of the following vowels - o | a | u | - it will have the same sound of the English G.</li>
<li>When G is followed by I or E&nbsp; - it will have the sound explained in the 1st line.</li>
<li>When G is followed by ue - the u&nbsp; becomes silent and you prounounce it as&nbsp; the normally English g but skipping the u as if e is the next word and u does not exist. This rule also applies to Q. You will pronounce the q as it is but skip the u and jump to e</li>
</ul>
