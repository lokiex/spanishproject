<br><br><a href="#top" class="top">Scroll to top</a><h4 id="3">C</h4>
<p><strong>Pronunciation</strong></p>
<p><em>English - seh</em></p>
<p><em>Spanish - ce</em></p>
<p><strong>Notes</strong></p>
<p>The position of the tongue while pronouncing c and z is almost the same. The English Z sound as in buzz does not exist in Spanish. For pronouncing c and z correctly try saying the English word Myth and once you reach "th" freeze don&rsquo;t move. Now from that position (i.e. your tongue touching the back of your 3 upper front teeth, the central incisors) try saying seh in English. You will use this sound for seh (c) and sehta(z). Example Barcelona (Barsehlona) and Zoo (soh), you will use the 'myth' tongue position explained above and say soo )</p>
<p class="4"><strong>CH - Chay in English and Che in Spanish</strong> <em>was once included as a seperate alphabet but it is no more considered as a seperate alphabet.</em></p>
