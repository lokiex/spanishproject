<h2 id="def">Lesson 7 - Specifying things</h2><br>

     We use articles to refer to specific nouns. For eg.<b>The man knew his job well</b>, here we are speaking about a specific person already identified previously. It is not the same to say that A Man knew his job which is not identifying which man knew his job. Unlike English we have a special word for using articles for each gender. Thus there is a masculine article and a feminine article. In Spanish the <b>masculine 'the'</b> is <b>el</b> and the <b>feminine 'the'</b> is <b>la</b>. If the noun being specified is plural then the <b>el</b> becomes <b>los</b> and the <b>la</b> becomes <b>las</b>. <h5>Grammatical Term</h5>These are called <b>Definite articles</b> in English and <b>Artículos definidos</b> in Spanish.<br><h5>Examples</h5> Let's check the examples below to understand this better.
<br><br>
<ul>
<li><b>The boy</b> is very intelligent. <b>El niño</b> es muy inteligente.</li>
<li><b>The house</b> has five windows. <b>La casa</b> tiene cinco ventanas.</li>
<li><b>The girls</b> are very intelligent. <b>Las niñas</b> son muy inteligentes.</li>
<li><b>The cars</b> in city are all red. <b>Los coches</b> de tu ciudad son todas rojas.</li>
</ul><br><br>
     <h2 id="indef">Generally speaking</h2> <a href="#top">Scroll to top</a>
<br><br>
Similarly we also find the need to speak about this in general without specifying something in specific. We use the word <b>a</b> for this purpose and if we need to use the plural form we use words such as <b>some</b> for this purpose. In Spanish again we have seperate words for both the genders. <b>a masculine</b> translates to <b>un</b> and <b>a feminine</b> translates to <b>una</b> in Spanish. The pluaral forms are <b>unos</b> and <b>unas</b>. <h5>Grammatical Term</h5>These are called <b>Indefinite articles</b> in English and <b>Artículos indefinidos</b> in Spanish.<br><h5>Examples</h5> Let's check the examples below to understand this better.
<br><br>
<ul>
<li><b>A</b> diamond is forever. <b>Un</b> diamante es para siempre.</li>
<li><b>A</b> school is a place to learn. <b>Una</b> escuela es un lugar para aprender.</li>
<li>Give me <b>some</b> books. Dame <b>unos</b> libros.</li>
<li>I can see <b>some</b> houses. Puedo ver <b>unas</b> casas.</li>
</ul>
