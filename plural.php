<br><br><h2 id="main">Lesson 9 - One or many</h2><br><br>
In Spanish, the simple rule to follow while converting nouns to plurals is by adding an <b>-s</b> to singular words ending in a vowel and adding an <b>-es</b> to those that end in consonants. Having said that, exceptions do exist but before we discuss them lets understand the simple rule using Examples below. Examples
<ul>
<li>La mes<b>a</b> - Las mes<b>as</b> / The table - The tables</li>
<li>La sill<b>a</b> -Las sill<b>as</b> / The chair - The chairs</li>
<li>La nacio<b>n</b> - Las nacio<b>nes</b> / The nation - The nations</li>
<li>El coch<b>e</b> - Los coch<b>es</b> / The car - The cars</li>
<li>El tre<b>n</b> - Los tren<b>es</b> / The train - The trains</li>
<li>El ordernado<b>r</b> - Los ordenador<b>es</b> / The computer - The computers</li>
</ul>
Note how the articles also change to plural.
<br><br><h2>The exceptions</h2><br><br>

<br><br><h4 id="1">Rule 1</h4><br><br>
If the word ends in an accented<b> &iacute;</b> and <b>&uacute;</b> then we add either an <b>-s</b> or <b>-es</b> except for spanish translation of Yes which is <b>S&iacute;</b> will become <b>S&iacute;es</b>
<ul>
<li>Bamb<b>&uacute;</b> - Bamb<b>&uacute;s</b> / Bamb&uacute;<b>es</b></li>
<li>Hind<b>&uacute;</b> - Hind<b>&uacute;s</b> / Hind&uacute;<b>es</b></li>
<li>Iran<b>&iacute;</b> - Iran<b>&iacute;s</b> / Iran&iacute;<b>es</b></li>
<li>Tis<b>&uacute;</b> - Tis<b>&uacute;s</b> / Tis&uacute;<b>es</b></li>
<li>marroqu<b>&iacute;</b> - marroqu<b>&iacute;</b>s / marroqu&iacute;<b>es</b></li>
</ul>
For words such as the spanish word for negative <b>no</b> we add an <b>-es</b> to it i.e. <b>noes</b>.
<br><br>
<h4 id="2">Rule 2</h4><br><br>
Nouns and adjectives ending in <b>-l,-r,-n,-d,-z</b> if they follow a vowel will take an <b>-es</b> suffix. For words ending in <b>-z</b>, the <b>-z </b>changes to <b>-c+es</b> Examples
<ul>
<li>pape<b>l</b> - papel<b>es</b> / paper - papers</li>
<li>amo<b>r</b> - amor<b>es</b> / love - loves</li>
<li>algod&oacute;<b>n</b> - algodon<b>es</b> / cotton - cottons</li>
<li>pare<b>d</b> - pared<b>es</b> / wall - walls</li>
<li>pe<b>z</b> - pe<b>ces</b> / fish - fish</li>
</ul>
The only exception would be words with 3 or more syllables which are accented on the third-to-last syllable, the plural form remains unchanged for these words. Examples el polis&iacute;ndeton - los polis&iacute;ndeton
<br><br>
<h4 id="3">Rule 3</h4><br><br>
Those nouns that <b>end in</b> consonants <b>-b -c -f -g -h -k -m -p -t -w</b> are usually words from other languages and for the plural form we <b>add -s</b> in the end except for the words <b>club</b> <b>imam</b> and <b>&aacute;lbum</b> for which we add <b>-es</b>. Please note that for the word club both <b>-s</b> and <b>-es</b> can be used to form the plural. Words ending with a <b>-ch</b> can also take <b>-es</b> as a suffix. Examples
<ul>
<li>el keb<b>ab</b> - los keb<b>abs</b></li>
<li>el bl<b>oc</b> - los bl<b>ocs</b></li>
<li>el / la ch<b>ef</b> - los / las ch<b>efs</b></li>
<li>el bl<b>og</b> - los bl<b>ogs</b></li>
<li>la hook<b>ah</b> - las hook<b>ahs</b></li>
<li>el bat<b>ik</b> - los bat<b>iks</b></li>
<li>el bo<b>om</b> - los bo<b>oms</b></li>
<li>el ch<b>ip</b> - los ch<b>ips</b></li>
<li>el tar<b>ot</b> - los tar<b>ots</b></li>
<li>el sh<b>ow</b> - los sh<b>ows</b></li>
</ul>
<br><br>
<h4 id="4">Rule 4</h4><br><br>
For words ending with <b>consonants clubbed together</b> we only add <b>-s</b>. For ease of prununciation the words ending in <b>st</b> and <b>zt</b> are left <b>unchanged</b> in their plural forms. These are primarily loan words from other languages such as <b>localhost</b>, <b>tes</b>t etc. We will only change the article to plural for such words eg. <b>los tes</b>t. Also the words <b>lord</b> and <b>milord</b> are an exception and are written as <b>lores</b> and <b>milores</b> in their plural forms. Examples
<ul>
<li>go<b>ng</b> - go<b>ngs</b></li>
<li>icebe<b>rg</b> - icebe<b>rgs</b></li>
<li>r&eacute;co<b>rd</b> - r&eacute;co<b>rds</b></li>
</ul>
<br><br>
<h4 id="5">Rule 5</h4><br><br>
For nouns and adjectives that <b>end in y</b> which follows a vowel we add <b>-es</b> Examples
<ul>
<li>r<b>ey</b> - r<b>eyes</b> / king - kings</li>
<li>l<b>ey</b> - l<b>eyes</b> / law - laws</li>
<li>bu<b>ey</b> - bu<b>eyes</b> / ox - oxen</li>
</ul>
<br><br>
<h4 id="6">Rule 6</h4><br><br>
For <b>some nouns</b> that are primarily <b>from other languages</b> we <b>change</b> the <b>y</b> to an <b>i</b> and add <b>-es</b> Examples
<ul>
<li>ga<b>y</b> - ga<b>is</b> / gay - gays</li>
<li>jerse<b>y</b> - jers&eacute;<b>is</b> / sweater - sweaters</li>
<li>espra<b>y</b> - espr&aacute;<b>is</b> / spray -sprays</li>
<li>y&oacute;que<b>y</b> - yoque<b>is</b> / jockey - jockeys</li>
</ul>
<br><br>
<h4 id="7">Rule 7</h4><br><br>
For <b>some nouns</b> that are from <b>other languages</b> and <b>end with a consonant followed by y</b>, we <b>change the y</b> to <b>i</b> and <b>add an -s.</b> Examples
<ul>
<li>dad<b>dy</b> - dan<b>dis</b></li>
<li>pan<b>ty</b> - pan<b>tis</b></li>
<li>fer<b>ry</b> - fer<b>ris</b></li>
</ul>
<br><br>
<h4 id="8">Rule 8</h4><br><br>
For the plural forms of nouns <b>ending</b> in <b>s</b> or <b>x</b> with a <b>single syllable</b> or with <b>more than one syllable</b> which has a <b>stress on the last syllable</b>, we <b>add</b> <b>-es</b>. The word <b>dux</b> would be an <b>exception</b> to this rule which will <b>remain unchanged</b> in it's plural form as <b>los dux</b>. Examples
<ul>
<li>to<b>s</b> - to<b>ses</b> / cough - coughs</li>
<li>val<b>s</b> - val<b>ses</b> / waltz - waltzes</li>
<li>fa<b>x</b> - fa<b>xes</b> / fax - faxes</li>
<li>comp&aacute;<b>s</b> - compa<b>ses</b> / compass - compasses</li>
<li>franc&eacute;<b>s</b> - france<b>ses</b> / french - french people</li>
</ul>
<br><br>
<h4 id="9">Rule 9</h4><br><br>
The singular and plural forms remain <b>unchanged</b> for nouns <b>ending in s or x with more than one syllable</b> and where the <b>stress is not on the last syllable</b>. Examples
<ul>
<li>la crisis - las crisis / the crisis - the crisis</li>
<li>el an&aacute;lisis - los an&aacute;lisis / the analysis - the analysis</li>
<li>el lunes - los lunes / Monday - Mondays</li>
<li>el t&oacute;rax - los t&oacute;rax / thorax - thorax</li>
<li>la caries - las caries / cavity - cavities</li>
</ul>
<br><br>
<h4 id="10">Rule 10</h4><br><br>
For words which are a <b>combination of 2 word</b>s, known as compound nouns, if the <b>last word is a plural nou</b>n then we leave the word <b>unchanged</b> even in the <b>plural form</b>.
<ul>
<li>el abrecartas - los abrecartas / the letter opener - the letter openers</li>
<li>el lavaplatos - los lavaplatos / the dishwasher - the dishwashers</li>
<li>el ciempi&eacute;s - los ciempi&eacute;s / the centipede - the centipedes</li>
</ul>
<br><br>
<h4 id="11">Rule 11</h4><br><br>
For nouns ending in <b>ch</b> all of which are words from other languages will either <b>remain unchanged</b> in their plural form <b>or</b> will take <b>-es</b> as a <b>suffix</b>. Examples
<ul>
<li>s&aacute;ndwi<b>ch</b> - los s&aacute;ndwi<b>ches</b> / los s&aacute;ndwi<b>ch</b></li>
</ul>
<br><br>
<h4 id="12">Rule 12</h4><br><br>
Those nouns that <b>end in accented vowels followed by</b> either <b>n</b> or <b>s los</b>e their <b>accents</b> in the <b>plural form</b>. Examples
<ul>
<li>El Pantal<b>&oacute;n</b> - Los Pantal<b>ones</b> / Trouser - Trousers</li>
</ul>
<br><br>
<h4 id="13">Rule 13</h4><br><br>
The <b>position</b> of the <b>accent</b> of following 3 words <b>changes</b> when converting to the <b>plural form</b>. Examples
<ul>
<li>esp<b>&eacute;</b>cim<b>en</b> - espec<b>&iacute;</b>me<b>nes</b> / Specimen - Specimens</li>
<li>r<b>&eacute;</b>gimen - reg<b>&iacute;</b>me<b>nes</b> / diet - diets</li>
<li>car<b>&aacute;</b>cter - caract<b>e</b>r<b>es</b> / character - characters</li>
</ul>
That brings this lesson to an end. Next we will learn about some basic words and sentences in Spanish.
