<ul>
<li>1001. mil un/uno/una</li>
<li>1048. mil cuarenta y ocho</li>
<li>1116. mil ciento diecis&eacute;is</li>
<li>2000. dos mil</li>
<li>3000. tres mil</li>
<li>10.000 - diez mil</li>
<li>30.400 - treinta mil cuatrocientos</li>
</ul>
