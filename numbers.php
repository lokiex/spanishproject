<li >100. cien</li>
<li>101. ciento uno / un /una</li>
</ul>
<br><a href="#top" class="top">Scroll to top</a><h2 id="7">Spanish numbers from 200 to 1000</h2>
<p>From two hundred onwards till nine hundred and nighty nine all the numbers have the following in common: the numbers are formed by number + cientos + remaining number (with the exception of 500) so 239 will be doscientos treinta y nueve. Observe how the hundreds are referred to as plural. For eg. two hundred is dosciento<strong>s</strong>. There are slight changes in how 700 and 900 are written and pronounced. Check it below.</p>
<ul>
<li>200. doscientos/doscientas</li>
<li>300. trescientos/-as</li>
<li>400. cuatrocientos/-as</li>
<li>500. quinientos/-as</li>
<li>600. seiscientos/-as</li>
<li>700. setecientos/-as</li>
<li>800. ochocientos/-as</li>
<li>900. novecientos/-as</li>
<li>1000. mil</li>
</ul>
<br><a href="#top" class="top">Scroll to top</a><h2 id="8">Counting in the thousands in Spanish</h2>
