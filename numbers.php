<br><a href="#top" class="top">Scroll to top</a><h2 id="10">Billion and above in Spanish</h2>
<p>The English billion and the Spanish billion are not the same. This can cause great confusion and it is important to note that the Spanish continue to count 1000 million after 999 million and so on. The Spanish start counting 1 billion where the English start with 1 trillion and so on. So pay attention to this below in the examples:</p>
<ul>
<li>1000.000.000 - Mil millones (Billion in US/UK English)</li>
<li>1000.000.000.000 - Un bill&oacute;n (Trillion in US/UK English)</li>
<li>1000.000.000.000.000 - Un trill&oacute;n (Quadrillion in US/UK English)</li>
</ul>
