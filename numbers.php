<p>Here is some useful information about numbers in Spanish</p>
<ul>
<li>The seperator for hundreds and thousands is denoted by a dot and not a coma</li>
<li>A coma is used as for decimals instead of a dot.</li>
<li>If it comes before a noun then <strong>Uno</strong> becomes <strong>Un</strong> and never uno</li>
<li>The quantities for 1 and 200 to 999 the numbers change their endings to agree with the gender of the noun they belong to</li>
