<h2 id="1">Naming the Numbers</h2>
<p><br />'Numero Uno' is probably one of the most well known loan words that the English language has taken from the Spanish Language. Spanish numbers are fun to learn, and for the most part, easy to remember. Some rules for decimals and seperators are exactly opposite of what we use in English and some numbers need to agree with the gender of the noun So as we continue we will explore the world of numbers in Spanish and know more about it.</p>
<br><a href="#top" class="top">Scroll to top</a><h2 id="2">Spanish numbers 0 to 10</h2>
<p>As is the case with the English numbers even with the Spanish numbers learning 1 to 9 makes learning the remaining numbers easier. So make sure you learn these first and very well. <br />0 Cero</p>
<ol>
<li>Uno</li>
<li>Dos</li>
<li>Tres</li>
<li>Cuatro</li>
<li>Cinco</li>
<li>Seis</li>
<li>Siete</li>
<li>Ocho</li>
<li>Nueve</li>
<li>Diez</li>
</ol>
<br><a href="#top" class="top">Scroll to top</a><h2 id="3">Spanish numbers 11 to 15</h2>
<p>Just like numbers 0 to 10, the numbers from 11 to 15 need to be memorized as they don't follow any particular pattern like rest of the numbers do.</p>
<ul>
<li>11. Once</li>
<li>12. Doce</li>
<li>13. Trece</li>
<li>14. Catorce</li>
<li>15. Quince</li>
</ul>
<br><a href="#top" class="top">Scroll to top</a><h2 id="4">Spanish numbers 16 to 20</h2>
<p>The easiest (and very obvious) way to learn numbers 16 to 19 is to understand that the spanish call it ten and six, ten and seven, ten and eight, ten and nine. So as we have already learnt, in Spanish ten is <strong>diez</strong> while the word <strong>and</strong> is translated as <strong>y</strong>, when we combine the <strong>diez</strong> and <strong>y</strong> to form <strong>dieci</strong> and simply add the single digit seis (6) or siete(7) or ocho(8) or nueve(9). Remember that seis will now have a tilde as we are adding <strong>dieci</strong> to it and changing the position of the alphabet that has to be stressed. Again we will have to memorize twenty <strong>veinte</strong> in Spanish.</p>
<ul>
<li>16. diecis&eacute;is</li>
<li>17. diecisiete</li>
<li>18. dieciocho</li>
<li>19. diecinueve</li>
<li>20. veinte</li>
</ul>
<br><a href="#top" class="top">Scroll to top</a><h2 id="5">Counting the 20s in Spanish</h2>
<p>We use the same logic as in 16 to 19 to memorize the twenties in spanish. The only difference is that we (obviously)use veinte instead of diez. The <strong>veinte</strong> changes to <strong>veinti</strong> after combining with <strong>y</strong>. Again note that there are some numbers which will have tilde. So let's check this below:</p>
<ul>
<li>21. veintiuno/un*/una*</li>
<li>22. veintid&oacute;s</li>
<li>23. veintitr&eacute;s</li>
<li>24. veinticuatro</li>
<li>25. veinticinco</li>
<li>26. veintis&eacute;is</li>
<li>27. veintisiete</li>
<li>28. veintiocho</li>
<li>29. veintinueve</li>
<li>30. treinta</li>
</ul>
<p><br />* We will learn about the change from uno to un or una later towards the end. Click <a href="#12">here</a>  if you can't wait.</p>
<br><a href="#top" class="top">Scroll to top</a><h2 id="6">Spanish numbers from 30 to 199</h2>
<p>The numbers 30-99 follow the a similar pattern to each other making it easier to memorize the numbers. We only need to memorize the 10th number after 30 (including 30 of course!)and repeat the same thereafter till 100 so we memorize 40,50,... and so on till 90. Unlike <strong>twenty one</strong> which is <strong>veintiuno</strong> we do not combine the numbers to form just one word and hence all the numbers till 99 use 3 words eg. <strong>thirty and one</strong> which in spanish is treinta y uno. So 42 will be cuarenta y dos and so on. When we have learnt how to say 100 <strong>cien</strong> (remember <strong>cent</strong> a latin root word). Thereafter all numbers will be exactly the same except that we will add <strong>ciento</strong> before the numbers. So 101 will be ciento uno, ciento dos, ciento diez right uptill ciento noventa y nueve.</p>
<ul>
<li>31. treinta y uno/un*/una*</li>
<li>32. treinta y dos</li>
<li>33. treinta y tres</li>
<li>34. treinta y cuatro</li>
<li>35. treinta y cinco</li>
