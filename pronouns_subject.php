<h2>Lesson 4 - You and the People</h2>

Welcome to this lesson about subject pronouns in Spanish. Jargons apart, subject pronouns are labels that we give to ourselves and people with whom we interact or speak about. Simply put it is about being able to say <b>I</b> <b>You</b> <b>He</b> <b>She</b> <b>We</b> <b>They</b> along with few more additions we do not have in the English language. So here we go:

<br><br>
<ul>
    <li><b>I - Yo</b></li> 

<li><b>You - Tú </b><i>informal</i></li>

<li><b>You - Usted</b><i> formal</i></li>

     <li><b>He - Él</b></li>

<li><b>She - Ella</b></li>

<li><b>We - Nosotros </b><i>male</i></li>

<li><b>We - Nosotras </b><i>female</i></li>

<li><b>We - Nosotros </b><i>mix</i></li>

<li><b>You all - Vosotros </b><i>informal. male</i></li>

<li><b>You all - Vosotras </b><i>informal. female</i></li>

<li><b>You all - Vosotros </b><i>informal. mix</i></li>

<li><b>They - Ellos </b><i>male</i></li>

<li><b>They - Ellas </b><i>female</i></li>

<li><b>They - Ellos </b><i>mix</i></li>

<li><b>You all - Ustedes </b><i>formal</i></li>
</ul>
<br>
Like many Indian languages and many other international languages Spanish has a way of formally addressing the You form (Usted) apart from an informal type (Tú). We use the Usted form when adressing people whom we are not friendly or familiar with or require to have a more formal relationship with. This is also used in order to address someone with respect for eg. elders.

Note how we also have a plural form of Usted - Ustedes ( You all) which is used to address a formal group of people Also note how the Spanish language has a special word for You all (informal) - Vosotros. Generally when we refer to ourselves as a mix of a group of males and females we use the masculine for for both. Eg. We - Nosotros. If this is not the case then we use Nosotros for all males and Nosotras for all females group. Generally when we address people in the 2nd person plural as a mix of a group of males and females we use the masculine for for both. Eg. You all - Vosotros. If this is not the case then we use Vosotros for all males and Vosotras for all females group. Generally when we address others as a mix of a group of males and females we use the masculine form for both. Eg. They - Ellos. If this is not the case then we use Ellos for all males and Ellas for all female groups.</p>
