<div class="col-7">Note how we also have a plural form of Usted - Ustedes ( You all) which is used to address a formal group of people
Also note how the Spanish language has a special word for You all (informal) - Vosotros.
Generally when we refer to ourselves as a mix of a group of males and females we use the masculine for for both. Eg. We - Nosotros. If this is not the case then we use Nosotros for all males and Nosotras for all females group.
Generally when we address people in the 2nd person plural as a mix of a group of males and females we use the masculine for for both. Eg. You all - Vosotros. If this is not the case then we use Vosotros for all males and Vosotras for all females group.
Generally when we address others as a mix of a group of males and females we use the masculine form for both. Eg. They - Ellos. If this is not the case then we use Ellos for all males and Ellas for all female groups.   
