<h2 id="1">Lesson 8 - Ladies & Gentlemen</h2><br>
Everything in Spanish has a Gender, whether living or non-living ! 

It is mostly possible to identify the gender by certain suffixes or 

endings of the noun. The most common endings for the masculine 

gender is <b>o</b> and that for the feminine gender is <b>a</b>. 

Life for a Spanish learner would have been far easier if this rule 

was a general one, but that's not the case. While most of the words 

follow this rule, not all of them do so. We will discuss all of 

this in the sections that follow below in order to learn the 

different suffixes that exists for each gender. A bit of practice 

will do the job. Let's start with the most basic suffix
<b>o</b> and <b>a</b>.


<h5>Grammatical Term</h5>Known as <b>The Gender</b> in English and 

<b>El Género </b> in Spanish.<br><h5>Examples</h5> Let's check the 

examples below to understand this better.
<h4> Masculine - Masculino </h4> 
<ul>
<li> A boy - Un Chic<b>o</b></li>
<li> the husband - el espos<b>o</b></li>
<li> the brother - el herman<b>o</b></li>
<li> the grandfather -  el abuel<b>o</b></li>
<li> the waiter - el camarer<b>o</b></li>
</ul>

<h4> Feminine - Femenino (note how the article also changes) </h4> 


<ul>
<li> A girl - Una Chic<b>a</b></li>
<li> the wife - la espos<b>a</b></li>
<li> the sister - la herman<b>a</b></li>
<li> the grandmother -  la abuel<b>a</b></li>
<li> the waitress - la camarer<b>a</b></li>
</ul>
<h2 id="2">Other endings</h2><br>
     <a href="#top">Scroll to top</a><br><br>
As discussed earlier, not all endings are as simple as <b>o</b> or 

<b>a</b> Some masculine words drop the vowel
<b>o</b> in the end while still keeping the <b>a</b> at the end of 

the feminine word
<br><br>
<ul>
<li> el profeso<b>r</b> - la profeso<b>ra</b> - (the 

teacher)</li>
<li> el pinto<b>r</b> - la pinto<b>ra</b> - (the 

painter)</li>
<li> el escrito<b>r</b> - la escrito<b>ra</b> - (the 

writer)</li>
<li> el ganado<b>r</b> - la ganado<b>ra</b> - (the 

winner)</li>
    <br><br>
The gender for some more complex endings need to be remembered but 

there are some tricks that can help identifying these as well. 

These are as follows:

<br><br>
<h4> Nouns that end in <b>-ción</b> are all feminine, so are those 

that end in <b>-sión</b> and <b>-xión</b></h4>
<ul>
<li> la fun<b>ción</b> - function</li>
<li> la motiva<b>ción</b> - motivation</li>
<li> la na<b>ción</b> - nation</li>
<li> la profe<b>sión</b> - profession </li>
<li> la exten<b>sión</b> - extension </li>
<li> la revi<b>sión</b> - revision </li>
<li> la cone<b>xión</b> - connection</li>
<li> la comple<b>xión</b> - complexion </li>
<li> la refle<b>xión</b> - reflection </li>
</ul>

<h4> Words ending in <b>-tad</b>,<b>-dad</b>,<b>-tud</b>, -

<b>umbre</b>, -ie are feminine.</h4>
<ul>
<li> la facul<b>tad</b> - faculty</li>
<li> la amis<b>tad</b> - friendship</li>
<li> la volun<b>tad</b> - will</li>
<li> la ciu<b>dad</b> - city</li>
<li> la bon<b>dad</b> - goodness</li>
<li> la felici<b>dad</b> - happiness </li>
<li> la alti<b>tud</b> - altitude </li>
<li> la solici<b>tud</b> - application / request </li>
<li> la magni<b>tud</b> - magnitude</li>
<li> la cost<b>umbre</b> - custom (tradition) </li>
<li> la incertid<b>umbre</b> - uncertainty </li>
<li> las leg<b>umbres</b> - pulses </li>
<li> la superfi<b>cie</b> - surface </li>
<li> la ser<b>ie</b> - series </li>
</ul>
Some of the words ending in <b>-ión</b> are feminine while some are 

masculine. Check the article <b>la</b> for feminine and <b>el</b> 

for masculine for the words below.
<ul>
<li> la acc<b>ión</b> - the action</li>
<li> el cam<b>ión</b> - the truck</li>
<li> el av<b>ión</b> - the airplane</li>
<li> la reg<b>ión</b> - the region</li>
<li> la opin<b>ión</b> - the opinion </li>
<li> el gu<b>ión</b> - the script</li>
</ul>



<h2 id="3">Animals</h2><br>
<a href="#top">Scroll to top</a>
In Spanish we either have completely different names for male and 

female animals or have similar names with slightly different 

endings. Some less common animals are referred to using the 

masculine form.
<br><br>
<ul> 
<li> el perr<b>o(male)</b> la perr<b>a(female)</b> The dog </li>
<li> el gat<b>o(male)</b>la gat<b>a(female)</b> The cat </li>
<li> el <b>toro(male)</b>la<b>la vaca(female)</b> Bull / Cow </li>
<li> el burr<b>o(male)</b>la burr<b>a(female)</b> The donkey </li>
<li> la <b>tortuga</b> (regardless of gender) The tortoise </li>
</ul>






<h2 id="4">Things</h2><br>
<a href="#top">Scroll to top</a>
As mentioned earlier, in Spanish everything has a gender even 

things which are either masculine or feminine, thus <b>la casa</b> 

- <b>the house</b> is feminine and 
<b>el coche</b> - <b> the car</b> is masculine. It would be a good 

practice to learn the articles that precede the words to keep in 

mind the gender of the words as the whole sentence is affected by 

the gender of the word as everything in the sentence, from the 

articles to the adjectives should agree with the noun. The rules 

explained earlier can also be used to easily learn the gender of 

things using the common suffixes for each gender. Some words are as 

follows:
<br><br>
<ul>
<li> <b>la silla</b> the chair</li>
<li> <b>la mesa</b> the table</li>
<li> <b>el edificio</b> the building</li>
<li> <b>los pantalones</b> the trousers</li>
<li> <b>el camino</b> the way</li>
</ul>





<h2 id="5">Common words</h2><br>
Most words that end in <b>-e</b> can be used for both the genders 

with a preceding <b>el</b> or <b>la</b>. Exceptions do exist such 

as Jef<b>e</b> / Jef<b>a</b>. Words ending is <b>-ista</b> also 

follow the same rule as above. There are some words ending in 

<b>o</b> that also follow the above rule. Examples include the 

following:<br><br>

<a href="#top">Scroll to top</a><br><br>
<ul>
<li> <b>el estudiante / la estudiante</b></li>
<li> <b>el gerente / la gerente</b></li>
<li> <b>el periodista / la periodista</b></li>
<li> <b>el especialista / la especialista</b></li>
<li> <b>el taxista/ la taxista</b></li>
<li> <b>el dentista/ la dentista</b></li>
<li> <b>el policía / la policía</b></li>
<li> <b>el miembro / la miembro</b></li>
<li> <b>el piloto / la piloto</b></li>
</ul>






<h2 id="6">Shared words with different meanings</h2><br>
<a href="#top">Scroll to top</a>
There are some exactly same words which change in meaning with the 

gender. Some common ones are as follows:
<br><br>
<ul>
<li> <b>el capital</b> - the money <b>la capital</b> - the capital 

city</li>
<li> <b>el coma</b> - the coma <b>la coma</b> - the comma </li>
<li> <b>el cura</b> - the priest <b>la cura</b> - the cure </li>
<li> <b>el guía</b> - the guide (masculine)<b>la guía</b> - the 

guide book / female guide </li>
<li> <b>el orden</b> - the order (as in arrangement <b>la orden</b> 

- the command </li>
<h2 id="7">Some exceptions</h2>
<a href="#top">Scroll to top</a>
<p>Now that we have learnt about different ways of identifying gender in Spanish, let us see some exceptions which do exist to the rules mentioned earlier.</p>
<p>Nouns ending in <b>-ma</b> are masculine and not feminine hence shoud be used with <b>el</b> and not <b>la</b>.</p>
<ul>
<li><b>el</b> siste<b>ma</b> - the system</li>
<li><b>el</b> dra<b>ma -&nbsp;&nbsp;</b>the drama</li>
<li><b>el</b> proble<b>ma -&nbsp;</b>the problem</li>
<li><b>el</b> trau<b>ma - </b>the trauma</li>
<li><b>el</b> progra<b>ma -&nbsp;</b>the program</li>
<li><b>el</b> cli<b>ma -&nbsp;</b>the climate</li>
<li><b>el</b> panora<b>ma -&nbsp;</b>the panorama</li>
<li><b>el </b>te<b>ma -&nbsp;</b>the theme / the topic</li>
</ul>
<p>Some nouns ending in <b>-a</b> and <b>-o</b> are masculine and feminine respectively.</p>
<ul>
<li><b>el</b> d&iacute;<b>a -</b>&nbsp; the day</li>
<li><b>el</b> planet<b>a - </b>the planet</li>
<li><b>el</b> map<b>a -&nbsp; </b>the map</li>
<li><b>el</b> cur<b>a - </b>the priest</li>
<li><b>el</b> comet<b>a - </b>the comet</li>
<li><b>el</b> sof<b>&aacute; - </b>the sofa</li>
</ul>
<p>Feminine nouns beginning with a stressed <b>a</b> or <b>ha&nbsp;</b>use&nbsp;<b>el&nbsp;</b>instead of&nbsp;<b>la&nbsp;</b>as the article. This is done to facilitate better pronunciation due to the issue that comes up with pronouncing the l<b><span style="text-decoration: underline;">a</span></b> <b><span style="text-decoration: underline;">a</span></b> combination. It is important to note that these words remain to be feminine and only the article changes to <b>el</b>, the adjectives and plurals still are used as per the general rules which we will study in the later lessons.</p>
<ul>
<li><b>el</b> <b>a</b>gua - water</li>
<li><b>el ha</b>mbre - hunger</li>
<li><b>el a</b>lma - the soul</li>
<li><b>el &aacute;</b>guila - the eagle</li>
<li><b>el a</b>sma - asthma</li>
<li><b>el a</b>rpa - the harp</li>
<li><b>el a</b>ma de casa - the housewife</li>
</ul>
